        ]);
    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $data = $request->validate([
            'title' => 'required|max:255',
            'content' => 'required',
            'featured_img' => 'nullable'
        ]);

        $post = Post::create($data);

        return response([
            'data' => [
                'post' => $post
            ],
            'msg' => 'Post created successfully'
        ], 201);
    }

